#import "KPButton.h"

static NSTimeInterval defaultInterval = 1.0;

@implementation KPButton

- (instancetype)init
{
    self = [super init];
    if (self) {
        self.acceptEventInterval = defaultInterval;
    }
    return self;
}

- (instancetype)initWithCoder:(NSCoder *)aDecoder
{
    self = [super initWithCoder:aDecoder];
    if (self) {
        self.acceptEventInterval = defaultInterval;
    }
    return self;
}

- (instancetype)initWithFrame:(CGRect)frame
{
    self = [super initWithFrame:frame];
    if (self) {
        self.acceptEventInterval = defaultInterval;
    }
    return self;
}

@end
